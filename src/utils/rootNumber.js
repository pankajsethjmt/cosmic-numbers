// ── GET ROOT DATA FROM DD ─────────────────────────────────
// Takes birth day (dd) as string or number, returns full root data.
// Mirrors getSunSign(dd, mm) pattern — can be used independently anywhere.
//
// Usage: getRootNumber("14") → { number: 5, nameEn: "Root Number 5", ... }
//        getRootNumber(28)   → { number: 1, nameEn: "Root Number 1", ... }

export function getRootNumber(dd) {
  const day = parseInt(dd, 10);
  if (isNaN(day) || day < 1 || day > 31) return null;

  // Reduce to single digit (same logic as calFunction.reduce)
  let root = day;
  while (root > 9) {
    root = String(root)
      .split("")
      .reduce((sum, d) => sum + parseInt(d, 10), 0);
  }

  const rootData = ROOT_NUMBERS.find((r) => r.number === root) ?? null;
  return rootData;
}
